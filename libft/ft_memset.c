#include "libft.h"

void	*ft_memset(void *str, int c, size_t len)
{
	size_t	i;
	char	*p;

	if (len == 0)
		return (str);
	p = str;
	i = 0;
	while (i < len)
	{
		*(p + i) = c;
		i++;
	}
	return (str);
}
