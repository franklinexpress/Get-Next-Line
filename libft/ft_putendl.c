#include "libft.h"

void	ft_putendl(char const *s)
{
	char endl;
	char c;

	c = 0;
	endl = '\n';
	while (*s)
	{
		c = *s;
		write(1, &c, 1);
		s++;
	}
	write(1, &endl, 1);
}
