#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	char			*d;
	const char		*s;
	size_t			n;
	int				dest_len;

	s = src;
	d = dst;
	n = 0;
	while ((n++ != size) && (*d != '\0'))
		d++;
	dest_len = d - dst;
	n = size - dest_len;
	if (n == 0)
		return (dest_len + ft_strlen(s));
	while (*s != '\0')
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (dest_len + (s - src));
}
